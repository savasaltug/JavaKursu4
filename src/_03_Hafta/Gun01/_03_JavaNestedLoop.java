package _03_Hafta.Gun01;

public class _03_JavaNestedLoop {
    public static void main(String[] args) {
        // asagiddaki goruntuyu veren programi yazin(yalnizca 1 yildiz kullanarak)
        //*****
        //****
        //***
        //**
        //*

        for (int i = 5; i >= 1; i--) {
            for (int j = 0; j<i; j++ ){
                System.out.print(" * ");

            }
            System.out.println();

        }
    }
}
