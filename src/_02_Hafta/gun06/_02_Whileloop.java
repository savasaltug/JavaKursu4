package _02_Hafta.gun06;

public class _02_Whileloop {
    public static void main(String[] args) {
        // ekrana 5 kez merhaba yazdiriniz.
        int sayac = 0;

        //0 1 2 3 4 -> 5 adim
        while(sayac<5) // sart saglandigi surece parantezlerinin arasini celistir
        {
            System.out.println("merhaba");
            sayac++;
        }
    }
}
